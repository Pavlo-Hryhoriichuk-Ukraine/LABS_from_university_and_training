def average_mark(dict_of_students:dict[tuple,dict], list_of_subjects: list[str]) -> dict[str,list]:
    """
    Docstring for average_mark
    
    :param dict_of_students: Description
    :type dict_of_students: dict[tuple, dict]
    :return: Description
    :rtype: dict[str, list]

    DETAILS: Summing students marks and translate it into average mark for each
    subject. Includes 2 functions, that helps with translating string marks.
    Returns a dict: keys: subjects names; values: list of 3-type average marks.
    """
    answer_dict = {}

    def translate_ECTS(text: str) -> str:
        dict_to_translate = {"A": 7, "B": 6, "C": 5, "D": 4, "E": 3, 'Fx': 2,"F": 1}
        full_sum = 0
        text = text.split(",")
        for elem in text:
            full_sum += dict_to_translate[elem]
        target = full_sum // 40
        return min(dict_to_translate, key= lambda x: abs(dict_to_translate[x]-target)) #found closest mark for our average in dict_to_translate
    
    def translate_UKR(ECTS: str) -> str:
        match ECTS:
            case "A":
                return "Відмінно"
            case "B":
                return "Дуже добре"
            case "C":
                return "Добре"
            case "D":
                return "Задовільно"
            case "E":
                return "Достатньо"
            case "Fx":
                return "Незадовільно"
            case "F":
                return "НЕЗАДОВІЛЬНО"

    for i in range (1,4):
        subject = list_of_subjects[i]
        subject_sum = 0
        subject_ECTS = ""
        for dict_student in dict_of_students.values():
            subject_sum += dict_student[f"{subject}"][0]
            subject_ECTS += dict_student[f"{subject}"][1] + ","
        lst_resoult = [subject_sum/40,ECTS := translate_ECTS(subject_ECTS), translate_UKR(ECTS)]
        answer_dict[f"{subject}"] = lst_resoult
    return answer_dict

def scholarship(dict_of_students:dict[tuple,dict]) -> list[tuple[str]]:
    """
    Docstring for scholarship
    
    :param dict_of_students: Description
    :type dict_of_students: dict[tuple, dict]

    DETAILS: Helps decide which of students can get a scholarship.
    Rules: 40% of students, that don`t have any "E" (ECTS) of worse mark.
    Returns sorted list with key= "marks" in descending order.
    """
    list_of_HIGH_students = []
    dict_of_scores = {}
    for key,value in dict_of_students.items():
        flag = True
        counter = 0
        dict_of_scores[key] = list()
        for subject in value.values():
            counter += 1
            if subject[0] <= 75:
                flag = False
            dict_of_scores[key].append(subject[0]) 
        if flag:
            list_of_HIGH_students.append(key)

    leN = len(dict_of_students) * 0.4
    while len(list_of_HIGH_students) >= leN:
        list_of_HIGH_students.pop()

    return sorted(list_of_HIGH_students,key=lambda x: sum(dict_of_scores[x]),reverse=True)

