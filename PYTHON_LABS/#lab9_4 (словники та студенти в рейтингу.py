def sort_by_ECTS(dict_of_students:dict[tuple,dict[str,list]]) -> dict[str,dict[str,list[tuple]]]:
    """
    Docstring for sort_by_ECTS
    
    :param dict_of_students: Description
    :type dict_of_students: dict[tuple, dict[str, list]]
    :return: Description
    :rtype: dict[str, dict[str, list[tuple]]]

    DETAIL: Sorting by ECTS marks in descending order.
    Returns dict: keys: ECTS marks, values: dicts: keys: subjects names,
    values: list of tuples, that includes surname and name of student, that
    has that mark.
    """
    dict_of_marks = {
                    "A": {"Матан": [],"Механіка":[],"Молекулярка":[]},
                     "B": {"Матан": [],"Механіка":[],"Молекулярка":[]},
                     "C":{"Матан": [],"Механіка":[],"Молекулярка":[]},
                     "D": {"Матан": [],"Механіка":[],"Молекулярка":[]},
                     "E": {"Матан": [],"Механіка":[],"Молекулярка":[]},
                     "Fx": {"Матан": [],"Механіка":[],"Молекулярка":[]},
                     "F": {"Матан": [],"Механіка":[],"Молекулярка":[]}
                     }

    for student,subjects in dict_of_students.items():
        for subject,mark in subjects.items():
            dict_of_marks[mark[1]][subject].append(tuple(list(student)[:2]))
    return dict_of_marks

